#!/usr/bin/env node
/* Splice curated hitch puzzles into the existing 200-level campaign,
   replacing a handful of levels within one chapter rather than growing
   the campaign — CHAPTER_SIZE, FREE_LEVELS/Pro gating, and unlock-index
   semantics in game.js all assume a fixed 200-level, 4×50 shape, so this
   keeps that shape intact instead of touching that surface area.

   Run after tools/generate-levels.mjs regenerates the base 200, or any
   time the hitch pool (js/generate.js: tryGenerateHitch) changes:
     node tools/gen-hitch-pool.mjs        # writes .genwork/hitch-pool.json
     node tools/add-hitch-levels.mjs      # splices + re-verifies

   Target chapter: Neon District (index 1, par band 15–18) — the first
   chapter past the intro ramp, so hitches land as a fresh mechanic once
   the base game is comfortable, not buried in expert content. */

import { writeFileSync, readFileSync, existsSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';
import { solve } from '../js/solver.js';

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..');
const POOL_FILE = join(ROOT, '.genwork', 'hitch-pool.json');
const OUT = join(ROOT, 'js', 'levels.data.js');

const TARGET_CHAPTER = 1;      // Neon District
const REPLACE_COUNT = 8;

if(!existsSync(POOL_FILE)){
  console.error(`No hitch pool at ${POOL_FILE} — run tools/gen-hitch-pool.mjs first.`);
  process.exit(1);
}

const { LEVELS, CHAPTERS, CHAPTER_SIZE, INTRO } = await import(pathToUrl(join(ROOT, 'js', 'levels.data.js')));
function pathToUrl(p){ return 'file://' + p + '?t=' + Date.now(); }   // bust the module cache on re-run

const band = CHAPTERS[TARGET_CHAPTER];
const pool = JSON.parse(readFileSync(POOL_FILE, 'utf8'));

const inBand = pool
  .filter(lv => lv.m >= band.minM && lv.m <= band.maxM)
  .sort((a, b) => a.d - b.d || a.m - b.m);
console.log(`Pool: ${pool.length} total, ${inBand.length} in ${band.name}'s band ${band.minM}-${band.maxM}.`);
if(inBand.length < REPLACE_COUNT){
  console.error(`Need ${REPLACE_COUNT} in-band hitch levels, only have ${inBand.length}. Widen the pool or the band.`);
  process.exit(1);
}

// Spread picks evenly across the in-band pool's own difficulty spread
// (not just the first N) so the 8 chosen levels aren't all clustered at
// one end of 15–18, and dedupe by key defensively.
const seen = new Set();
const picks = [];
for(let j = 0; j < REPLACE_COUNT; j++){
  let idx = Math.min(inBand.length - 1, Math.floor(inBand.length * (j + 0.5) / REPLACE_COUNT));
  while(seen.has(inBand[idx].key) && idx < inBand.length - 1) idx++;
  seen.add(inBand[idx].key);
  picks.push(inBand[idx]);
}
console.log('Picked pars:', picks.map(l => l.m).join(', '));

const chStart = TARGET_CHAPTER * CHAPTER_SIZE;
const chapterLevels = LEVELS.slice(chStart, chStart + CHAPTER_SIZE);

// Evenly-spaced slots within the chapter's own 50, so replaced originals
// (and thus the hitch levels standing in for them) are spread through the
// chapter's progression rather than bunched at one end.
const slotIdx = Array.from({ length: REPLACE_COUNT }, (_, j) =>
  Math.floor(CHAPTER_SIZE * (j + 0.5) / REPLACE_COUNT));
const mixed = chapterLevels.slice();
slotIdx.forEach((slot, j) => { mixed[slot] = picks[j]; });

// Re-sort the whole chapter by score so the hitch levels land wherever
// their own difficulty places them, restoring a monotonic-ish progression
// instead of just sitting at the arbitrary slot indices above.
mixed.sort((a, b) => a.d - b.d || a.m - b.m);

const newLevels = LEVELS.slice(0, chStart).concat(mixed, LEVELS.slice(chStart + CHAPTER_SIZE));
if(newLevels.length !== LEVELS.length) throw new Error(`level count drifted: ${newLevels.length} vs ${LEVELS.length}`);

console.log('Re-verifying all 200 levels (including the 8 new hitch levels)…');
newLevels.forEach((lv, i) => {
  const pieces = lv.p.map(a => ({ r: a[0], c: a[1], len: a[2], dir: a[3] }));
  const sol = solve(pieces, { walls: lv.w, gates: lv.g, hitches: lv.h });
  if(!sol.solvable || sol.optimal !== lv.m){
    throw new Error(`level ${i + 1} failed verification (par ${lv.m}, solved ${sol.solvable ? sol.optimal : 'unsolvable'})`);
  }
});
console.log('All verified.');

const levelsJs = `/* AUTO-GENERATED by tools/generate-levels.mjs, then tools/add-hitch-levels.mjs
   spliced in ${REPLACE_COUNT} hitch puzzles — do not edit by hand. 200 levels,
   verified optimal (m = par), curved by difficulty model v1 score (d).
   Chapters are palette-swap themed for now (full environments arrive in
   v1.5 per the sequencing plan). ${band.name} (chapter ${TARGET_CHAPTER + 1})
   carries this batch's hitch puzzles — pieces marked in a level's \`h\`
   array need towing/decoupling to clear, per js/solver.js's hitch rules. */

export const CHAPTER_SIZE = ${CHAPTER_SIZE};

/* Levels 1–INTRO ease in below chapter 1's floor; every later level needs ≥ minM moves. */
export const INTRO = ${INTRO};

export const CHAPTERS = ${JSON.stringify(CHAPTERS, null, 2)};

export const LEVELS = [
${newLevels.map(lv => JSON.stringify({
    m: lv.m, d: lv.d, p: lv.p,
    ...(lv.w?.length ? { w: lv.w } : {}),
    ...(lv.g?.length ? { g: lv.g } : {}),
    ...(lv.h?.length ? { h: lv.h } : {}),
  })).join(',\n')}
];
`;

writeFileSync(OUT, levelsJs);
console.log(`Wrote ${OUT}`);
console.log(`${band.name} now spans levels ${chStart + 1}-${chStart + CHAPTER_SIZE}, ${REPLACE_COUNT} of them hitch puzzles.`);
