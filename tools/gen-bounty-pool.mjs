#!/usr/bin/env node
/* Curate the nightly Bounty rotation (HEIST-PLAN.md §6, phase H4) from the
   Fogleman import surplus — the 405 verified par 41-60 boards that didn't
   make it into Gridlock's splice (tools/add-fogleman-levels.mjs). No new
   generator work: every board here is already exhaustively verified.

   Run after tools/import-fogleman.mjs has produced the pool:
     node tools/import-fogleman.mjs tools/data/fogleman-boards.txt \
       --min-par 41 --out .genwork/fogleman-pool.json
     node tools/gen-bounty-pool.mjs

   Par buckets match the reward tiers in js/collection.js (common/
   uncommon/rare/legendary) — see js/bounty.js's PAR_BUCKETS, which this
   script's bucket definitions must stay in sync with. */

import { writeFileSync, readFileSync, existsSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';
import { mulberry32, hashStr } from '../js/rng.js';
import { solve } from '../js/solver.js';

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..');
const POOL_FILE = join(ROOT, '.genwork', 'fogleman-pool.json');
const OUT = join(ROOT, 'js', 'bounty-rotation.data.js');

const BUCKETS = [
  { name: 'common', lo: 41, hi: 45, count: 30 },
  { name: 'uncommon', lo: 46, hi: 50, count: 20 },
  { name: 'rare', lo: 51, hi: 55, count: 10 },
  { name: 'legendary', lo: 56, hi: 60, count: 4 },
];

if(!existsSync(POOL_FILE)){
  console.error(`No pool at ${POOL_FILE} — run tools/import-fogleman.mjs first (see header).`);
  process.exit(1);
}

const { LEVELS } = await import(pathToUrl(join(ROOT, 'js', 'levels.data.js')));
function pathToUrl(p){ return 'file://' + p + '?t=' + Date.now(); }

const pool = JSON.parse(readFileSync(POOL_FILE, 'utf8'));
const usedInCampaign = new Set(LEVELS.map(l => JSON.stringify(l.p)));
const unused = pool.filter(l => !usedInCampaign.has(JSON.stringify(l.p)));
console.log(`Pool: ${pool.length} total, ${unused.length} not already shipped in the campaign.`);

const picks = [];
for(const b of BUCKETS){
  const inBucket = unused.filter(l => l.m >= b.lo && l.m <= b.hi).sort((a, c) => a.d - c.d || a.m - c.m);
  const n = Math.min(b.count, inBucket.length);
  if(n < b.count) console.log(`  ${b.name}: only ${inBucket.length} available, taking all of them.`);
  // Spread evenly across the bucket's own difficulty range rather than
  // just the first N, so e.g. "common" isn't all bunched at par 41.
  for(let j = 0; j < n; j++){
    const idx = Math.min(inBucket.length - 1, Math.floor(inBucket.length * (j + 0.5) / n));
    picks.push({ ...inBucket[idx], tier: b.name });
  }
}
console.log(`Curated ${picks.length} boards: ${BUCKETS.map(b => `${b.name} ${picks.filter(p => p.tier === b.name).length}`).join(', ')}`);

// Shuffle deterministically so consecutive nights aren't clustered by tier
// (the picks above are grouped bucket-by-bucket) — reproducible on rerun.
const rng = mulberry32(hashStr('mg-bounty-rotation-v1'));
for(let i = picks.length - 1; i > 0; i--){
  const j = Math.floor(rng() * (i + 1));
  [picks[i], picks[j]] = [picks[j], picks[i]];
}

console.log('Re-verifying every picked board…');
picks.forEach((lv, i) => {
  const pieces = lv.p.map(a => ({ r: a[0], c: a[1], len: a[2], dir: a[3] }));
  const sol = solve(pieces, { walls: lv.w });
  if(!sol.solvable || sol.optimal !== lv.m){
    throw new Error(`rotation slot ${i}: verification failed (par ${lv.m}, solved ${sol.solvable ? sol.optimal : 'unsolvable'})`);
  }
});
console.log('All verified.');

const rotationJs = `/* AUTO-GENERATED by tools/gen-bounty-pool.mjs — do not edit by hand.
   ${picks.length} boards curated from Michael Fogleman's exhaustively-
   enumerated Rush Hour database (michaelfogleman.com/rush), independently
   re-solved and verified by our own solver, drawn from the surplus left
   over after tools/add-fogleman-levels.mjs's Gridlock splice. js/bounty.js
   cycles this array deterministically by date — same pattern as the daily
   puzzle, no backend. 'tier' matches a par bucket (common 41-45, uncommon
   46-50, rare 51-55, legendary 56-60) and drives which collection car a
   cleared bounty can award (js/collection.js). */

export const BOUNTY_ROTATION = [
${picks.map(lv => JSON.stringify({
    m: lv.m, d: lv.d, p: lv.p,
    ...(lv.w?.length ? { w: lv.w } : {}),
    tier: lv.tier,
  })).join(',\n')}
];
`;

writeFileSync(OUT, rotationJs);
console.log(`Wrote ${OUT}`);
