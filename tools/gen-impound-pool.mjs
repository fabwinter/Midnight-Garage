#!/usr/bin/env node
/* Curate the Impound Lot (v2.0 "Featured Boards", pulled forward — see
   docs/NEXT-PLAN.md N2) from whatever's left of the Fogleman import
   surplus after Gridlock's splice (tools/add-fogleman-levels.mjs) and the
   Bounty rotation (tools/gen-bounty-pool.mjs) have taken their share.

   Unlike the bounty rotation (date-cycled, one board a night) the Impound
   Lot is a browsable list players work through at their own pace once
   unlocked, so it's sorted into an ascending difficulty curve rather than
   shuffled, and each entry keeps its solver `key` — save progress here is
   keyed by board identity, not array position, so a future re-curation
   can't silently wipe or scramble a player's stars.

   Run after tools/import-fogleman.mjs has produced the pool:
     node tools/import-fogleman.mjs tools/data/fogleman-boards.txt \
       --min-par 41 --out .genwork/fogleman-pool.json
     node tools/gen-impound-pool.mjs */

import { writeFileSync, readFileSync, existsSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';
import { solve } from '../js/solver.js';

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..');
const POOL_FILE = join(ROOT, '.genwork', 'fogleman-pool.json');
const OUT = join(ROOT, 'js', 'impound-lot.data.js');
const TARGET_COUNT = 100;

if(!existsSync(POOL_FILE)){
  console.error(`No pool at ${POOL_FILE} — run tools/import-fogleman.mjs first (see header).`);
  process.exit(1);
}

const { LEVELS } = await import(pathToUrl(join(ROOT, 'js', 'levels.data.js')));
const { BOUNTY_ROTATION } = await import(pathToUrl(join(ROOT, 'js', 'bounty-rotation.data.js')));
function pathToUrl(p){ return 'file://' + p + '?t=' + Date.now(); }

const pool = JSON.parse(readFileSync(POOL_FILE, 'utf8'));
const usedCampaign = new Set(LEVELS.map(l => JSON.stringify(l.p)));
const usedBounty = new Set(BOUNTY_ROTATION.map(l => JSON.stringify(l.p)));
const unused = pool.filter(l => !usedCampaign.has(JSON.stringify(l.p)) && !usedBounty.has(JSON.stringify(l.p)));
console.log(`Pool: ${pool.length} total, ${unused.length} left after the campaign and bounty rotation took their share.`);

// Spread evenly across the remaining pool's own score distribution (not
// just its par, which is heavily front-loaded at 41-44) so the curated
// set still reaches its hardest tail instead of averaging it away.
const byScore = unused.slice().sort((a, b) => a.d - b.d || a.m - b.m);
const n = Math.min(TARGET_COUNT, byScore.length);
const seen = new Set();
const picks = [];
for(let j = 0; j < n; j++){
  let idx = Math.min(byScore.length - 1, Math.floor(byScore.length * (j + 0.5) / n));
  while(seen.has(byScore[idx].key) && idx < byScore.length - 1) idx++;
  if(seen.has(byScore[idx].key)) continue;
  seen.add(byScore[idx].key);
  picks.push(byScore[idx]);
}

// Display order: an ascending difficulty curve (par, then score) — this is
// what the player actually sees when working through the list.
picks.sort((a, b) => a.m - b.m || a.d - b.d);
console.log(`Curated ${picks.length} boards, par range ${picks[0].m}-${picks[picks.length - 1].m}.`);

console.log('Re-verifying every picked board…');
picks.forEach((lv, i) => {
  const pieces = lv.p.map(a => ({ r: a[0], c: a[1], len: a[2], dir: a[3] }));
  const sol = solve(pieces, { walls: lv.w });
  if(!sol.solvable || sol.optimal !== lv.m){
    throw new Error(`impound slot ${i}: verification failed (par ${lv.m}, solved ${sol.solvable ? sol.optimal : 'unsolvable'})`);
  }
});
console.log('All verified.');

const outJs = `/* AUTO-GENERATED by tools/gen-impound-pool.mjs — do not edit by hand.
   ${picks.length} boards curated from Michael Fogleman's exhaustively-
   enumerated Rush Hour database (michaelfogleman.com/rush), independently
   re-solved and verified by our own solver, drawn from whatever's left of
   the import surplus after the Gridlock splice and the Bounty rotation.
   js/game.js's Impound Lot (unlocked once Pro + all 200 campaign levels
   are cleared) lists these in this ascending-difficulty order. Each
   entry's 'key' is what save.impound.stars/best are keyed by — NOT array
   index, so re-running this script can reorder or replace entries without
   scrambling existing player progress on the ones that survive. */

export const IMPOUND_LOT = [
${picks.map(lv => JSON.stringify({
    m: lv.m, d: lv.d, p: lv.p,
    ...(lv.w?.length ? { w: lv.w } : {}),
    key: lv.key,
  })).join(',\n')}
];
`;

writeFileSync(OUT, outJs);
console.log(`Wrote ${OUT}`);
